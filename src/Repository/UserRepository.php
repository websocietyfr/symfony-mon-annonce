class UserRepository extends ServiceEntityRepository
{
    private $passwordHasher;

    public function __construct(ManagerRegistry $registry, UserPasswordHasherInterface $passwordHasher)
    {
        parent::__construct($registry, User::class);
        $this->passwordHasher = $passwordHasher;
    }

    public function hashPassword($password,$user) {
        return $this->passwordHasher->hashPassword(
            $user,
            $password
        );
    }

    public function createUser($firstname,$lastname,$email,$phone_number,$profil_picture,$password): User
    {
        $user = new User();
        $user->firstname = $firstname;
        $user->lastname = $lastname;
        $user->email = $email;
        $user->phone_number = $phone_number;
        $user->profil_picture = $profil_picture;
        $user->password = $this->passwordHasher->hashPassword(
            $user,
            $password
        );
        return $user;
    }

    public function add(User $entity, bool $flush = false): void
    {
        $this->getEntityManager()->persist($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
